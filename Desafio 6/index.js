function somar(numero1, numero2) {
    return numero1 + numero2
}
console.log('O resultado da soma é:', somar(1, 28))

function subtrair(numero1, numero2) {
    return numero1 - numero2
}
console.log('A subtração dos dois números é:',subtrair(10, 5))

function multiplicar(numero1, numero2) {
    return numero1 * numero2   
}
console.log('A multiplicação dos dois números é:', multiplicar(2, 4))

function dividir(numero1, numero2) {
    return numero1 / numero2
}
console.log('A divisão dos dois números é:', dividir(12, 2))